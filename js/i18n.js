  t(key) {
    return this.strings[this.lang]?.[key] || this.strings.en[key] || key;
  },

  setLang(lang) {
    this.lang = lang;
    this.dir = lang === 'fa' ? 'rtl' : 'ltr';
    document.documentElement.dir = this.dir;
    document.documentElement.lang = lang;
    document.title = this.t('appName');
    const preloader = document.getElementById('app');
    if (preloader) preloader.dir = this.dir;
  },

  init() {
    const saved = Storage?.getSettings()?.language;
    this.setLang(saved || 'fa');
  }
